"""Split each multi-panel pit-stop PNG into individual subplot PNGs.

The existing combined images were generated by visualize.py as 2x2 or
1x3 matplotlib grids.  This utility detects the white gutters between
sub-axes, then crops each sub-axis to its own PNG sized for solo
inclusion in the LaTeX report.

Per-file output naming uses a letter suffix (a/b/c/d) matching the
subplot label in the original combined image.
"""

